# ── Helper functions ──────────────────────────────────────────────────────────
def get_greeting() -> str:
    return random.choice(GREETINGS)

def get_emotion_ack(emotion: str) -> str:
    options = EMOTION_ACK.get(emotion, [""])
    return random.choice(options)

def get_question(intent: str, question_idx: int) -> str:
    intent_qs = QUESTIONS.get(intent, [["خبرني أكثر عن المشكلة؟"]])
    if question_idx >= len(intent_qs):
        return random.choice(intent_qs[-1])
    return random.choice(intent_qs[question_idx])

def get_confirm_question(intent: str) -> str:
    options = CONFIRM_QUESTIONS.get(intent, ["تبي أكمل في هذا الإجراء؟"])
